    private void SUBMITActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SUBMITActionPerformed
        // TODO add your handling code here:
       
        int maths=Integer.parseInt(jTextField2.getText());
        int os=Integer.parseInt(jTextField1.getText());
        int dbms=Integer.parseInt(jTextField5.getText());
        int ds=Integer.parseInt(jTextField3.getText());
        try{
            //            Class.forName("com.mysql.cj.jdbc.Driver");
            //            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/studentresult","root","srinadh");

            String url="jdbc:mysql://localhost:3306/studentresult";
            Connection conn=DriverManager.getConnection(url,"root","srinadh");//gives object of connection
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery("select rollno from studenthome where rollno='"+rolln+"'");
            if(rs.next()){
                st.executeUpdate("insert into eee_result values('"+rolln+"','"+maths+"','"+os+"','"+dbms+"','"+ds+"')");
                JOptionPane.showMessageDialog(null,"Data updated successfully");
                setVisible(false);
                new index().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Student is not registerd");
                setVisible(false);
                setVisible(true);
            }}
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e.toString());
            }
    }//GEN-LAST:event_SUBMITActionPerformed

