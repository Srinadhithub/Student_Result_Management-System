    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown

        jf9.setText(roll);
        String rollno=roll;
        
          try{      
String url="jdbc:mysql://localhost:3306/studentresult";
Connection conn=DriverManager.getConnection(url,"root","srinadh");//gives object of connection
Statement st=conn.createStatement();

ResultSet rs;
if(branch.equals("CSE"))
       rs=st.executeQuery("select *from studenthome inner join cse_result where studenthome.rollno='"+rollno+"' and cse_result.rollno='"+rollno+"'");
else rs=st.executeQuery("select *from studenthome inner join eee_result where studenthome.rollno='"+rollno+"' and eee_result.rollno='"+rollno+"'");
    if(rs.next()){
        jTextField4.setText(rs.getString(2));
          jTextField2.setText(rs.getString(3));
            jTextField3.setText(rs.getString(5));
             jTextField5.setText(rs.getString(7));
              jTextField6.setText(rs.getString(8));
               jTextField1.setText(rs.getString(9));
                jTextField7.setText(rs.getString(10));
                int sum=0;
                 jTextField9.setText("PASS");
                for(int i=7;i<=10;i++){
                    int val=Integer.parseInt(rs.getString(i));
                    sum+=val;
                    if(val<35){
                        jTextField9.setText("FAIL");
                    }
                }
     jTextField8.setText(String.valueOf(sum));
    }
          }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_formComponentShown

